package at.ALDA.BinarySearch;

public class Car {
    private String name;
    private int key;

    public Car(String name, int key) {
        this.name = name;
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getKey() {
        return key;
    }
}
